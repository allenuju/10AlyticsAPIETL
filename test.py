def add_nums():
    # add two numbers
    var1 = 5
    var2 = 10
    sum_of_num = var1 + var2
    print(f"The sum of numbers is {sum_of_num }")

add_nums()
    
def multiply_nums():
    # multiplication of two numbers
    var1 = 5
    var2 = 10
    mult_of_num = var1 * var2
    print(f"The product of numbers is {mult_of_num }")
    
multiply_nums()
    
